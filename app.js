function renderFlow() {
  renderSummary();
  const stages = getFlowStages();

  if (!state.selectedActionId) {
    flowTitle.textContent = t("guidedFlow");
    flowCopy.textContent = t("guidedFlowCopy");
    flowProgress.innerHTML = "";
    flowStageLabel.textContent = t("waitingForAction");
    stageHost.className = "stage-host empty-state";
    stageHost.textContent = t("selectPrimaryAction");
    backStepButton.disabled = true;
    nextStepButton.disabled = true;
    commitButton.disabled = true;
    commitButton.textContent = state.editingPlayId ? t("saveChanges") : t("recordPlay");
    return;
  }

  const activeIndex = Math.min(state.currentStageIndex, Math.max(0, stages.length - 1));
  state.currentStageIndex = activeIndex;
  const activeStage = stages[activeIndex];

  flowTitle.textContent = `${getActionLabel(state.selectedActionId)} ${t("flowSuffix")}`;
  flowCopy.textContent = activeStage.description;
  flowStageLabel.textContent = t("stepOf", { current: activeIndex + 1, total: stages.length });
  flowProgress.innerHTML = stages
    .map((stage, index) => {
      const classes = [
        "progress-pill",
        index === activeIndex ? "is-active" : "",
        isStageComplete(stage) ? "is-complete" : "",
      ].filter(Boolean).join(" ");
      return `<span class="${classes}">${stage.shortLabel}</span>`;
    })
    .join("");

  stageHost.className = "stage-host";
  stageHost.innerHTML = "";
  renderStage(activeStage);

  backStepButton.disabled = activeIndex === 0;
  nextStepButton.disabled = activeIndex >= stages.length - 1 || !isStageComplete(activeStage);
  commitButton.disabled = !isFlowComplete();
  commitButton.textContent = state.editingPlayId ? t("saveChanges") : t("recordPlay");
}

function handleFlowInputChange(changedStageIndex = state.currentStageIndex) {
  pruneConditionalContext();

  const stages = getFlowStages();
  if (!stages.length) {
    render();
    return;
  }

  const lastIndex = Math.max(0, stages.length - 1);
  if (changedStageIndex < lastIndex) {
    state.currentStageIndex = Math.min(changedStageIndex + 1, lastIndex);
    render();
    return;
  }

  normalizeFlowState({ advanceCompleted: true });

  if (state.autoRecordLastDetail && isFlowComplete()) {
    commitPlay();
    return;
  }

  render();
}

