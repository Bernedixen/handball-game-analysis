function commitPlay() {
  if (!isFlowComplete()) {
    return;
  }

  const player = getSelectedPlayer();
  const actionId = state.selectedActionId;
  const existingPlay = state.editingPlayId
    ? state.plays.find((play) => play.id === state.editingPlayId) ?? null
    : null;
  const preserveImportedMetadata = Boolean(existingPlay?.imported && existingPlay.actionId === actionId);
  const playId = existingPlay?.id ?? crypto.randomUUID();
  const timestamp = existingPlay ? new Date(existingPlay.time) : new Date();
  const derivedEvents = deriveEvents({
    playId,
    player,
    actionId,
    context: { ...state.context },
    timestamp,
  });

  const play = {
    id: playId,
    gameId: existingPlay?.gameId ?? "demo-game",
    time: timestamp.toISOString(),
    timeLabel: existingPlay?.timeLabel ?? formatTime(timestamp),
    playerId: player.id,
    actionId,
    context: { ...state.context },
    sourceText: preserveImportedMetadata ? existingPlay.sourceText : `${player.name} -> ${actionId}`,
    label: preserveImportedMetadata ? existingPlay.label : `${player.name} ${actionId.replace("_", " ")}`,
    contextTokens: buildPlayContextTokens({ existingPlay, context: state.context, preserveImportedMetadata }),
    imported: preserveImportedMetadata,
  };

  if (existingPlay) {
    const playIndex = state.plays.findIndex((entry) => entry.id === existingPlay.id);
    state.plays[playIndex] = play;
    state.events = state.events.filter((event) => event.playId !== existingPlay.id);
    state.events.push(...derivedEvents);
  } else {
    state.plays.push(play);
    state.events.push(...derivedEvents);
  }

  resetFlow();
  render();
}

function resetFlow() {
  state.selectedPlayerId = null;
  state.selectedActionId = null;
  state.context = {};
  state.editingPlayId = null;
  state.currentStageIndex = 0;
  render();
}

function getContextGroups(actionId) {
  if (!actionId) {
    return [];
  }

  const groups = (contextDefinitions[actionId] ?? []).filter((group) => isContextEnabled(actionId, group.key));

  if (actionId === "SHOT" && state.context.outcome === "GOAL" && isContextEnabled(actionId, "assistPlayerId")) {
    groups.push({
      key: "assistPlayerId",
      label: getContextLabel("assistPlayerId"),
      options: ["NONE", ...players.filter((player) => player.id !== state.selectedPlayerId).map((player) => player.id)],
    });
  }

  return groups;
}

